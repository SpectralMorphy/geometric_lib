"""Calculations for square"""

def area(a: float) -> float:
    """Find area of the square
    
    Args:
        a: Side of the square

    Returns:
        Area of the square
    """
    return a * a


def perimeter(a: float) -> float:
    """Find perimeter of the square
    
    Args:
        a: Side of the square

    Returns:
        Perimeter of the square
    """
    return 4 * a
